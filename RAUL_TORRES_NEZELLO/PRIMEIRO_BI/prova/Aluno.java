package RAUL_TORRES_NEZELLO.PRIMEIRO_BI.prova;

public class Aluno {
    private String nome;
    private String sobrenome;

    public Aluno(String nome, String sobrenome){
        this.nome = nome;
        this.sobrenome = sobrenome;
    }

    public String getNome() {
        return nome;
    }

    public String getSobrenome() {
        return sobrenome;
    }
    
}
